let REG_1 = /(?!^)(?=(\d{3})+?$)/g
;[
  '1',
  '123',
  '1234',
  '12345',
  '123456',
  '1234567',
  '12345678',
  '123456789',
  '1234567890'
].forEach((i) => console.log(i.replace(REG_1, ',')))
let REG_2 = /(?!$)(?<=\.(\d{3})+?)/g
;[
  '0.1',
  '0.123',
  '0.1234',
  '0.12345',
  '0.123456',
  '0.1234567',
  '0.12345678',
  '0.123456789',
  '0.1234567890'
].forEach((i) => console.log(i.replace(REG_2, ',')))
const REG_3 = /(?!$)(?<=^(\d{3})+?)/g
;[
  '123.123',
  '1234.1234',
  '12345.12345',
  '123456.123456',
  '1234567.1234567',
  '12345678.12345678',
  '123456789.123456789',
  '1234567890.1234567890',
  '1234567890'
].forEach((i) =>
  console.log(
    /\./.test(i)
      ? [
          i.split('.')[0].replace(REG_1, ','),
          i.split('.')[1].replace(REG_3, ',')
        ].join('.')
      : i.replace(REG_1, ',')
  )
)
